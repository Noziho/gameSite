<?php

use App\Model\Entity\AbstractEntity;

class Role extends AbstractEntity
{
    public string $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): self
    {
        $this->name = $name;
        return $this;
    }


}